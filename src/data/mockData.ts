export const formatCurrency = (amount: number, currency: string) => {
  return new Intl.NumberFormat('es-PE', {
    style: 'currency',
    currency,
    minimumFractionDigits: 2,
  }).format(amount)
}

export const formatDate = (timestamp: number) => {
  return new Intl.DateTimeFormat('es-PE', {
    dateStyle: 'long',
    timeStyle: 'short',
  }).format(new Date(timestamp))
}

export const formatShortDate = (timestamp: number) => {
  const now = Date.now()
  const diff = now - timestamp
  const mins = Math.floor(diff / 60000)
  if (mins < 60) return `hace ${mins} min`
  const hours = Math.floor(mins / 60)
  if (hours < 24) return `hace ${hours} h`
  return new Intl.DateTimeFormat('es-PE', { dateStyle: 'medium' }).format(new Date(timestamp))
}

export const formatTime = (timestamp: number) => {
  return new Intl.DateTimeFormat('es-PE', {
    hour: '2-digit',
    minute: '2-digit',
    hour12: false,
  }).format(new Date(timestamp))
}

export function paymentToDeliveryOrder(
  payment: PaymentOrder,
  approvedBy: string,
): DeliveryOrder {
  return {
    id: `del_${payment.id.replace('pay_', '')}`,
    paymentOrderId: payment.id,
    purchaseNumber: payment.purchaseNumber,
    customerName: payment.customerName,
    customerDNI: payment.customerDNI,
    customerPhone: payment.customerPhone,
    deliveryAddress: payment.deliveryAddress,
    products: payment.products,
    totalAmount: payment.totalAmount,
    currency: payment.currency,
    paymentMethod: payment.paymentMethod,
    deliveryStatus: 'ready',
    shippingMethod: null,
    assignedDriver: null,
    shalomData: null,
    shalomTracking: null,
    approvedBy,
    approvedAt: Date.now(),
  }
}
